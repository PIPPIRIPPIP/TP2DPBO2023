    // Method untuk menambahkan data
    public void insertData(){
        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Data Yang Anda Masukkan Sudah Benar?", "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(confirm == JOptionPane.YES_OPTION){
            // jika user memilih ya maka akan masuk ke proses selanjutnya

            // mengambil data dari frame
            String id = idToko.getText();
            String nama = namaToko.getText();
            String almt = alamat.getText();
            String pass = password.getText();
            if(!id.isEmpty() && !nama.isEmpty() && !pass.isEmpty() && destinationFile != null){
                String foto = destinationFile.toString();
                foto = foto.replace("\\", "\\\\");
                // membuat query untuk memasukkan data
                String sql = "INSERT INTO toko (id_toko, nama, foto, alamat, pass) VALUES "
                        + "('"+id+"', '"+nama+"', '"+foto+"', '"+almt+"', '"+pass+"')";
                // menjalankan query
                db.updateQuery(sql);
                
                JOptionPane.showMessageDialog(null, "Berhasil menambahkan data!");
                
                // berpindah ke frame login
                this.dispose();
                Login login = new Login();
                login.setVisible(true);
            }else{
                 JOptionPane.showMessageDialog(null, "Isikan Data Secara Lengkap!");   
            }  
        }else{
            resetForm();
        }
    }
    
